use crate::{latency, latency_n};
use std::time::Duration;

/// An infinite iterator that encapsulates `K` closures and, for each invocation
/// of `next()`, yields an array of size `K` with wall-clock latency durations associated with
/// invocations of each of the `K` closures.
///
/// The iterator doesn't have to be infinite, though normally it would be. If a finite iterator is used
/// with the benchmarking functions, the benchmark will end prematurely if the iterator is exhausted
/// before the specified benchmark run length.
pub trait LatencySrc<const K: usize>: Iterator<Item = [Duration; K]> {
    /// Returns an iterator that yields the sum of each `[Duration; K]` generated by `self`.
    fn aggregate(&mut self) -> impl Iterator<Item = Duration> {
        self.map(|arr| arr.iter().sum())
    }
}

impl<const K: usize, T: LatencySrc<K>> LatencySrc<K> for &mut T {}

/// A [`LatencySrc`] that yields the latency of the invocation of a single closure on each
/// call to `next()`.
pub struct LatencySrc1<F: FnMut()> {
    f: F,
}

impl<F: FnMut()> LatencySrc1<F> {
    /// Returns an instance of `Self`.
    pub fn new(f: F) -> Self {
        Self { f }
    }
}

impl<F: FnMut()> Iterator for LatencySrc1<F> {
    type Item = [Duration; 1];

    #[inline(always)]
    fn next(&mut self) -> Option<Self::Item> {
        Some([latency(&mut self.f)])
    }
}

impl<F: FnMut()> LatencySrc<1> for LatencySrc1<F> {}

/// A [`LatencySrc`] that yields the latencies of the invocations of two closures on each
/// call to `next()`.
pub struct LatencySrc2<F1: FnMut(), F2: FnMut()> {
    f1: F1,
    f2: F2,
}

impl<F1: FnMut(), F2: FnMut()> LatencySrc2<F1, F2> {
    /// Returns an instance of `Self`.
    pub fn new(f1: F1, f2: F2) -> Self {
        Self { f1, f2 }
    }
}

impl<F1: FnMut(), F2: FnMut()> Iterator for LatencySrc2<F1, F2> {
    type Item = [Duration; 2];

    #[inline(always)]
    fn next(&mut self) -> Option<Self::Item> {
        Some([latency(&mut self.f1), latency(&mut self.f2)])
    }
}

impl<F1: FnMut(), F2: FnMut()> LatencySrc<2> for LatencySrc2<F1, F2> {}

/// Helper iterator that doles out group averages for batched latency sources.
struct Doler<V> {
    batch: u32,
    i: u32,
    value: V,
}

impl<V: Copy> Doler<V> {
    fn new(batch: u32, init_value: V) -> Self {
        Self {
            batch: batch.max(1),
            i: batch,
            value: init_value,
        }
    }

    #[inline(always)]
    fn update(&mut self, value: V) {
        self.value = value;
        self.i = 0
    }
}

impl<V: Copy> Iterator for Doler<V> {
    type Item = V;

    #[inline(always)]
    fn next(&mut self) -> Option<Self::Item> {
        if self.i < self.batch {
            self.i += 1;
            Some(self.value)
        } else {
            None
        }
    }
}

fn div_mod(dur: Duration, n: u32) -> (Duration, Duration) {
    let quotient = dur / n;
    let remainder = dur - quotient * n;
    (quotient, remainder)
}

/// A [`LatencySrc`] that batches invocations of a single `f` into groups of size `batch` and yields
/// the average latency of each group `batch` times before proceding to the next group execution.
pub struct LatencySrc1b<F: FnMut()> {
    /// Target closure
    f: F,
    /// Left from previous division
    residual: Duration,
    /// doler
    doler: Doler<[Duration; 1]>,
}

impl<F1: FnMut()> LatencySrc1b<F1> {
    /// Returns an instance of `Self`.
    pub fn new(f: F1, batch: u32) -> Self {
        Self {
            f,
            residual: Duration::ZERO,
            doler: Doler::new(batch, [Duration::ZERO]),
        }
    }
}

impl<F: FnMut()> Iterator for LatencySrc1b<F> {
    type Item = [Duration; 1];

    #[inline(always)]
    fn next(&mut self) -> Option<Self::Item> {
        match self.doler.next() {
            ret @ Some(_) => ret,
            None => {
                let batch = self.doler.batch;
                // adding the previous residual to the batch latency to reduce accumulation of truncation error
                let (value, residual) =
                    div_mod(latency_n(&mut self.f, batch) + self.residual, batch);
                self.residual = residual;
                self.doler.update([value]);
                self.doler.next()
            }
        }
    }
}

impl<F1: FnMut()> LatencySrc<1> for LatencySrc1b<F1> {}

/// A [`LatencySrc`] that batches invocations of two closures` into groups of size `batch` and yields
/// the respective average latencies of the closures for each group `batch` times before proceding
/// to the next group execution..
pub struct LatencySrc2b<F1: FnMut(), F2: FnMut()> {
    f1: F1,
    f2: F2,
    residual1: Duration,
    residual2: Duration,
    doler: Doler<[Duration; 2]>,
}

impl<F1: FnMut(), F2: FnMut()> LatencySrc2b<F1, F2> {
    /// Returns an instance of `Self`.
    pub fn new(f1: F1, f2: F2, batch: u32) -> Self {
        Self {
            f1,
            f2,
            residual1: Duration::ZERO,
            residual2: Duration::ZERO,
            doler: Doler::new(batch, [Duration::ZERO; 2]),
        }
    }
}

impl<F1: FnMut(), F2: FnMut()> Iterator for LatencySrc2b<F1, F2> {
    type Item = [Duration; 2];

    #[inline(always)]
    fn next(&mut self) -> Option<Self::Item> {
        match self.doler.next() {
            ret @ Some(_) => ret,
            None => {
                let batch = self.doler.batch;
                // adding the previous residual to the batch latency to reduce accumulation of truncation error
                let (value1, residual1) =
                    div_mod(latency_n(&mut self.f1, batch) + self.residual1, batch);
                let (value2, residual2) =
                    div_mod(latency_n(&mut self.f2, batch) + self.residual2, batch);
                self.residual1 = residual1;
                self.residual2 = residual2;
                self.doler.update([value1, value2]);
                self.doler.next()
            }
        }
    }
}

impl<F1: FnMut(), F2: FnMut()> LatencySrc<2> for LatencySrc2b<F1, F2> {}

#[cfg(feature = "_test_support")]
pub mod test_support {
    use super::*;
    use basic_stats::normal::lognormal_rand_gen;
    use std::array;

    /// Empty latency source.
    pub struct EmptyLatencySrc<const K: usize>;

    impl<const K: usize> Iterator for EmptyLatencySrc<K> {
        type Item = [Duration; K];

        fn next(&mut self) -> Option<Self::Item> {
            None
        }
    }

    impl<const K: usize> LatencySrc<K> for EmptyLatencySrc<K> {}

    /// Infinite source that always yields the same value. Useful for troubleshooting.
    pub struct ConstLatencySrc<const K: usize> {
        latencies: [Duration; K],
    }

    impl<const K: usize> ConstLatencySrc<K> {
        pub fn new(latencies: [Duration; K]) -> Self {
            Self { latencies }
        }
    }

    impl<const K: usize> Iterator for ConstLatencySrc<K> {
        type Item = [Duration; K];

        fn next(&mut self) -> Option<Self::Item> {
            Some(self.latencies)
        }
    }

    impl<const K: usize> LatencySrc<K> for ConstLatencySrc<K> {}

    /// An infinite iterator that encapsulates `K` iterators, each of which emits [`Duration`] values according to
    /// a lognormal distribution.
    ///
    /// This iterator's `next()` method yields a `[Duration; K]`, each component of which draws from the corresonding
    /// encapsulated iterator.
    pub struct LognormalLatencySrc<const K: usize> {
        generators: [Box<dyn Iterator<Item = Duration>>; K],
    }

    impl<const K: usize> LognormalLatencySrc<K> {
        /// Instantiates `Self`.
        ///
        /// The argument is an array of pairs of targets and sigmas that determine the probability distributions of the
        /// `K` component iterators:
        /// - each target is the target median latency for the component;
        /// - each sigma is the standard deviation for the natural logarithm of the component's probability distribution.
        pub fn new(targets_sigmas: [(Duration, f64); K]) -> Self {
            assert!(
                targets_sigmas
                    .iter()
                    .all(|(m, s)| *m > Duration::ZERO && *s > 0.0),
                "all medians and sigmas must be positive"
            );

            let generators = targets_sigmas.map(|(target, sigma)| {
                let gen_f64 = lognormal_rand_gen(target.as_secs_f64().ln(), sigma)
                    .expect("`target medians` and `sigmas` must all be `> 0`");
                let gen_dur = gen_f64.map(|v| Duration::from_secs_f64(v));
                let boxed_gen: Box<dyn Iterator<Item = Duration>> = Box::new(gen_dur);
                boxed_gen
            });

            Self { generators }
        }

        /// Instantiates `Self` with default sigmas for the underlying lognormal distributions.
        ///
        /// The argument is an array of the target medians of the `K` component iterators.
        /// The distributions' sigmas are set to a default value.
        pub fn new_with_default_sigmas(targets: [Duration; K]) -> Self {
            // At 2 standard deviations of the underlying normal distribution, the latency is `multiplier` times the median latency.
            const MULTIPLIER: f64 = 1.15;
            let default_sigma = MULTIPLIER.ln() / 2.;

            assert!(
                targets.iter().all(|m| *m > Duration::ZERO),
                "all `target medians` must be positive"
            );

            let targets_sigmas = targets.map(|m| (m, default_sigma));
            Self::new(targets_sigmas)
        }
    }

    impl<const K: usize> Iterator for LognormalLatencySrc<K> {
        type Item = [Duration; K];

        fn next(&mut self) -> Option<Self::Item> {
            let durations = array::from_fn(|i| {
                self.generators[i]
                    .next()
                    .expect("`lognormal_detm_gen(,,).next()` can't return `None`")
            });
            Some(durations)
        }
    }

    impl<const K: usize> LatencySrc<K> for LognormalLatencySrc<K> {}

    /// Infinite iterator that genearates durations that converge to a `target_latency`.
    pub struct ConvergingDurationIterator {
        target_latency: Duration,
        iteration: u64,
    }

    impl ConvergingDurationIterator {
        pub fn new(target_latency: Duration) -> Self {
            Self {
                target_latency,
                iteration: 0,
            }
        }
    }

    impl Iterator for ConvergingDurationIterator {
        type Item = Duration;

        fn next(&mut self) -> Option<Self::Item> {
            self.iteration += 1;
            let sign = if self.iteration % 2 == 1 { 1.0 } else { -1.0 };
            let value = self
                .target_latency
                .mul_f64(1.0 + sign / self.iteration as f64);
            Some(value)
        }
    }

    /// Latency source that encapsulates an array of [`ConvergingLatencyIterator`]s.
    pub struct ConvergingLatencySrc<const K: usize> {
        iters: [ConvergingDurationIterator; K],
    }

    impl<const K: usize> ConvergingLatencySrc<K> {
        pub fn new(target_latencies: [Duration; K]) -> Self {
            Self {
                iters: target_latencies.map(|ltn| ConvergingDurationIterator::new(ltn)),
            }
        }

        pub fn target_latencies(&self) -> [Duration; K] {
            array::from_fn(|k| self.iters[k].target_latency)
        }
    }

    impl<const K: usize> Iterator for ConvergingLatencySrc<K> {
        type Item = [Duration; K];

        fn next(&mut self) -> Option<Self::Item> {
            let opts = array::from_fn(|k| self.iters[k].next());
            if opts.iter().all(|v| v.is_some()) {
                let res = opts.map(|v| v.expect("must be Some"));
                Some(res)
            } else {
                None
            }
        }
    }

    impl<const K: usize> LatencySrc<K> for ConvergingLatencySrc<K> {}

    #[cfg(test)]
    mod test {
        use super::*;
        use crate::{BenchCfg, multi::BenchOut, rel_approx_eq_dur};
        use basic_stats::rel_approx_eq;
        use std::panic::catch_unwind;

        #[test]
        // cargo test --package bench_utils --lib --all-features -- multi::latency_src::test_support::test::test_lognormal_src --exact --nocapture --include-ignored
        fn test_lognormal_src() {
            const EPSILON: f64 = 0.002;
            const SAMP_SIZE: usize = 1000;

            let cfg = BenchCfg::default();
            let targets = [
                Duration::from_nanos(10),
                Duration::from_micros(20),
                Duration::from_millis(30),
            ];
            let src = LognormalLatencySrc::new_with_default_sigmas(targets);
            let out = BenchOut::from_iter(&cfg, src.take(SAMP_SIZE));
            let out_medians = out.medians();

            println!(
                "*** src[0]={:?}",
                LognormalLatencySrc::new_with_default_sigmas(targets)
                    .take(20)
                    .collect::<Vec<_>>()
            );

            rel_approx_eq_dur!(targets[0], out_medians[0], EPSILON);
            rel_approx_eq_dur!(targets[1], out_medians[1], EPSILON);
            rel_approx_eq_dur!(targets[2], out_medians[2], EPSILON);
        }

        #[test]
        fn test_const_latency_src() {
            let mut src =
                ConstLatencySrc::<2>::new([Duration::from_millis(1), Duration::from_millis(2)]);
            for _ in 0..5 {
                assert_eq!(
                    src.next(),
                    Some([Duration::from_millis(1), Duration::from_millis(2)])
                );
            }

            let mut src2 =
                ConstLatencySrc::<2>::new([Duration::from_millis(1), Duration::from_millis(2)]);
            let sum: Duration = src2.aggregate().take(3).sum();
            assert_eq!(sum, Duration::from_millis(9));
        }

        #[test]
        fn test_empty_latency_src() {
            let mut src = EmptyLatencySrc::<1>;
            assert_eq!(src.next(), None);

            let mut src2 = EmptyLatencySrc::<1>;
            assert_eq!(src2.aggregate().next(), None);

            let cfg = BenchCfg::default();
            let out = BenchOut::from_iter(&cfg, EmptyLatencySrc::<1>);
            assert_eq!(out.n(), 0);
        }

        #[test]
        fn test_converging_latency_src() {
            let target = Duration::from_millis(10);
            let src = ConvergingLatencySrc::<1>::new([target]);
            assert_eq!(src.target_latencies(), [target]);

            let items: Vec<[Duration; 1]> = src.take(10).collect();
            let mut prev_gap = f64::MAX;
            for (i, item) in items.iter().enumerate() {
                let dur = item[0];
                let above = dur > target;
                assert!(
                    above == (i % 2 == 0),
                    "iteration {i}: {dur:?} should be {} target",
                    if i % 2 == 0 { "above" } else { "below" }
                );
                let gap = if above { dur - target } else { target - dur };
                let gap_secs = gap.as_secs_f64();
                assert!(
                    gap_secs < prev_gap,
                    "iteration {i}: gap {gap_secs} not smaller than prev {prev_gap}"
                );
                prev_gap = gap_secs;
            }

            let src2 = ConvergingLatencySrc::<1>::new([target]);
            let total: Duration = src2.take(1000).map(|arr| arr[0]).sum();
            let mean_secs = total.as_secs_f64() / 1000.0;
            let target_secs = target.as_secs_f64();
            rel_approx_eq!(mean_secs, target_secs, 0.01);
        }

        #[test]
        fn test_lognormal_latency_src_new() {
            let cfg = BenchCfg::default();
            let target = Duration::from_millis(10);
            let src = LognormalLatencySrc::<1>::new([(target, 0.2)]);
            let out = BenchOut::from_iter(&cfg, src.take(2000));
            let medians = out.medians();
            rel_approx_eq_dur!(medians[0], target, 0.05);

            let result = catch_unwind(|| {
                LognormalLatencySrc::<1>::new([(Duration::ZERO, 0.2)]);
            });
            assert!(result.is_err(), "expected panic for zero median");

            let result = catch_unwind(|| {
                LognormalLatencySrc::<1>::new([(Duration::from_millis(10), 0.0)]);
            });
            assert!(result.is_err(), "expected panic for zero sigma");
        }
    }
}
